# Program untuk mencetak angka dari 1 sampai 10 menggunakan looping

# Menggunakan loop for untuk mencetak angka dari 1 sampai 10
print("Mencetak angka dari 1 sampai 10:")
for i in range(1, 11):
    print(i)

# Menggunakan loop while untuk mencetak angka dari 1 sampai 10
print("\nMencetak angka dari 1 sampai 10 menggunakan while loop:")
j = 1
while j <= 10:
    print(j)
    j += 1
